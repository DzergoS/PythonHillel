"""5. У математиці функцію `sign(x)` (знак числа) визначено так:

   ``

   sign(x) = 1, якщо x > 0,



   sign(x) = -1, якщо x < 0,



   sign(x) = 0 якщо x = 0.

   ``

   Для цього числа x виведіть значення sign(x).
   Це завдання бажано вирішити за допомогою каскадних інструкцій if... elif... else."""

digit = int(input("Введіть число: "))

if digit > 0:
    print(1)
if digit < 0:
    print(-1)
if digit == 0:
    print(0)